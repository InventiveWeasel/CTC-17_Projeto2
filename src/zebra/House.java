package zebra;

public class House {
	private int color;
	private int nation;
	private int drink;
	private int animal;
	private int cigar;
	
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	public int getNation() {
		return nation;
	}
	public void setNation(int nation) {
		this.nation = nation;
	}
	public int getDrink() {
		return drink;
	}
	public void setDrink(int drink) {
		this.drink = drink;
	}
	public int getAnimal() {
		return animal;
	}
	public void setAnimal(int animal) {
		this.animal = animal;
	}
	public int getCigar() {
		return cigar;
	}
	public void setCigar(int cigar) {
		this.cigar = cigar;
	}
	
}
